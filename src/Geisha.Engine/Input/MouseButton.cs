namespace Geisha.Engine.Input;

/// <summary>
///     Specifies mouse buttons.
/// </summary>
public enum MouseButton
{
    /// <summary>
    ///     Left mouse button.
    /// </summary>
    Left,

    /// <summary>
    ///     Middle mouse button.
    /// </summary>
    Middle,

    /// <summary>
    ///     Right mouse button.
    /// </summary>
    Right,

    /// <summary>
    ///     First extended mouse button.
    /// </summary>
    XButton1,

    /// <summary>
    ///     Second extended mouse button.
    /// </summary>
    XButton2
}